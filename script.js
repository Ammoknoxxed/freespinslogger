// Funktion zum Aktualisieren der Summe
function updateSummary() {
  let sum = 0;
  document.querySelectorAll(".fs-win-input").forEach(inp => {
    const v = parseFloat(inp.value);
    if (!isNaN(v)) sum += v;
  });
  document.getElementById("sumFsWin").textContent = sum.toFixed(2);
}

// Wenn eine FS-Gewinn-Eingabe sich ändert, neu berechnen
